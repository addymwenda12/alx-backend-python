#!/usr/bin/env python3
"""Function that returns the sum of two arguments"""

def add(a: float, b: float) -> float:
    """
    Adds two float numbers and returns the result as a float.

    :param a: The first float number.
    :param b: The second float number.
    :return: The sum of the two float numbers.
    """
    return (a + b)
